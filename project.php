        <div>
            <h3>Minecraft Modding</h3>
            <p style="padding:0em 1em;">These were mods we created for the game <a href="https://minecraft.net/">minecraft</a> that no longer are supported. </p>
             <ul class="products">
                <li>  
                    <h4><a href="http://calclavia.com/resonant-induction/">Resonant Induction</a></h4>
                    <p style="padding:0em 1em;"> 
                        Originally one of our join projects between calcalvia and our team. We stopped supporting the mod near the end of 2014 when joint designs could no longer exist. The mod itself adds micro brick gears, wires, and various mutli block machines. Designed to allow more creativity and puzzle building over single purpose machines.
                    </p> 
                </li>
                 <li>  
                    <h4><a href="#">Fluid Mechanics</a></h4>
                    <p style="padding:0em 1em;"> 
                        Was one of the original Universal Electricity mods, and part of a mod called steam power. Was split off aiming at fluid support for UE based mods. Most of the content from the mod was merged into Resonant Induction. The goal was to reduce our work load while increasing the support for our users. The tank, pipe, gutter, drain, pump, and fluid blocks in RI come from this mod.
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">Basic Components</a></h4>
                    <p style="padding:0em 1em;"> 
                        Created for the UE-Api as both an example and a simple machine mod. It was decommissions with parts being merged into several mods. Resonant induction and Resonant Engine picked up a few of these pieces. Galaticraft's machines were derived from this project though they have changed a bit since then. Basic components itself was decommissioned after a mod called Mekanism was released to replace its need.
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">Greater Protection</a></h4>
                    <p style="padding:0em 1em;"> 
                        Original created by out asset developer hangcow it was picked up and support by our modding team. It was let go after our team couldn't find time to maintain the mod. Before it was let got the mod added locked chests, locked doors, electric fence, laser fence, reinforced blocks, traps, and other useful security features. 
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">Illus Elements</a></h4>
                    <p style="padding:0em 1em;"> 
                        Created by a friend of the dev team Elrath18 and one of our own hangcow this mod added colored glass to minecraft. After several MC updates and other mods with the same idea it was let go. With the new 1.8 updated adding coloured glass the idea to revive it was completely forgotten.
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">ICBM-Sentries</a></h4>
                    <p style="padding:0em 1em;"> 
                        Sub mod for ICBM that added automated and player mounted weapon platforms. Included cannon, laser, rail, and AA sentry gun. Was decommissioned during the 1.7 update of ICBM in order to spread the content into other mods. Most likely will be recreated in both Tinker toys and Artillects. Most likely both will use a common system in Resonant Engine later on.
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">Guardsmen</a></h4>
                    <p style="padding:0em 1em;"> 
                        Created back in the beta days of MC this mod added simple NPC guards. The mod was continues up to 1.4.7 were the project files were lost. Rather than recoding the project from scratch it was abandoned. Concepts from the mod will be added to artillects. Which will include the NPC, barracks, and armory systems.
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">GSM Empires</a></h4>
                    <p style="padding:0em 1em;"> 
                        Early attempt to create an AI driven mod aimed at creating NPC villages that could run themselves. The project was cancelled but is currently being reworked as the Artillects project. 
                    </p> 
                </li>
                <li>  
                    <h4><a href="#">DarkLib</a></h4>
                    <p style="padding:0em 1em;"> 
                        Simple API and prefab system for mods mods created by our team before Resonant Engine was created. Was merged into calclavia core that later got renamed to resonant engine. Before merging it contained resonant engines early block network and content creation systems. It also includes basic ore and item generation that was similiar to Resonant Engines auto generation system.
                    </p> 
                </li>
