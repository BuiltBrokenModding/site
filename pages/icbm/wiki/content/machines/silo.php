<h2> Silo </h2>
<p> The silo is designed to be a static missile launching system. Built in several different sizes supporting each unique <a href="https://en.wikipedia.org/wiki/Missile">missile</a> set. Each silo has slighltly different requirements for missiles, launching systems, and setup. All silos require a <a href="/pages/icbm/wiki/content/machines/controller.php">controller</a> in order to fire missiles.</p>
<h2> Small Silo </h2>
<p> Small silo supports firing a single <a href="/pages/icbm/wiki/content/missiles/small.php">small missile</a> and is 3 blocks tall. It requires a solid surface to be placed and has no GUI. Missile can be inserted by right clicking and removed the same way.</p>
