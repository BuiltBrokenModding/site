    <h2>Missile</h2>
    <p>Most common used <a href="https://en.wikipedia.org/wiki/Missile">missile</a>, and standard for any destructive mission. </p>
    <img src="../../../../../img/icbm/standardMissile/launcher.png">
    <h3>Crafting</h3>
    <p>Standard missile has a unique crafting system not used in many mods. All the parts are inserted into the launcher to create both the frame, guts, and skin of the missile.</p>
    <img src="../../../../../img/icbm/standardMissile/frame.png">
    <p>The frame of the missile is created using 128 iron rods. These are inserted by right click on the missile or launcher CPU.</p>
    <img src="../../../../../img/icbm/standardMissile/frame-internals.png">
    <p>Once the frame is completed the engine, and guidance can be inserted. Warhead is optional but can not be inserted after the missile is completed.</p>
    <img src="../../../../../img/icbm/standardMissile/complete.png">
    <p>Skin is assembled from 36 iron plates. These are inserted by right clicking on the missile or launcher CPU.</p>
    <p>Once completed right click missile or launcher CPU to finish the missile. This will update the model with the completed version including a nice green texture. </p>
